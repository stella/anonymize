#!/usr/bin/env node
/**
 * Make the napi-rs-generated WASI glue load under Bun as well as Node.
 *
 * napi-rs emits a Node loader (`index.wasi.cjs`) and its threads worker
 * (`wasi-worker.mjs`) that build their WASI from `node:wasi`. The wasm binding
 * is a *reactor* module, so emnapi calls `wasi.initialize(instance)` on it.
 * Node's `node:wasi` WASI implements `.initialize()`; Bun's does not (it has
 * only `.start()`), so the binding throws `wasi.initialize is not a function`
 * and never loads under Bun.
 *
 * `@napi-rs/wasm-runtime` (already a dependency, already used by the generated
 * *browser* loader) ships a portable JS WASI that implements `.initialize()` and
 * runs on every runtime. This patch rewrites the two Node-target loaders to use
 * that WASI instead of `node:wasi`, passing it the same `node:fs` the loaders
 * already import so its `preopens` still work. Output is byte-identical between
 * Node and Bun; see `smoke-wasm-runtimes.mjs`, which runs the smokes under both.
 *
 * The loaders are build output (regenerated by `napi build`), so this runs as a
 * post-build step in `build-native-wasm.mjs`. Every rewrite asserts its anchor
 * text is present and throws if not: a napi-rs codegen change then fails the
 * build loudly instead of silently shipping the `node:wasi` version again.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

/** Replace `needle` with `replacement` in `source`, or throw naming `file`. */
function replaceOrThrow(source, needle, replacement, file) {
  if (!source.includes(needle)) {
    throw new Error(
      `patch-wasm-loader-runtime: anchor not found in ${file}:\n  ${needle}\n` +
        `napi-rs codegen likely changed; update patch-wasm-loader-runtime.mjs ` +
        `(and re-check the Bun smoke).`,
    );
  }
  return source.replace(needle, replacement);
}

/** Patch the Node loader `index.wasi.cjs` in `nativeDistDir`. */
function patchNodeLoader(nativeDistDir) {
  const file = join(nativeDistDir, "index.wasi.cjs");
  let code = readFileSync(file, "utf8");
  code = replaceOrThrow(
    code,
    "const { WASI: __nodeWASI } = require('node:wasi')",
    "const { WASI: __nodeWASI } = require('@napi-rs/wasm-runtime')",
    "index.wasi.cjs",
  );
  code = replaceOrThrow(
    code,
    "const __wasi = new __nodeWASI({\n  version: 'preview1',",
    "const __wasi = new __nodeWASI({\n  version: 'preview1',\n  fs: __nodeFs,",
    "index.wasi.cjs",
  );
  writeFileSync(file, code);
  return "index.wasi.cjs";
}

/** Patch the threads worker `wasi-worker.mjs` in `nativeDistDir`. */
function patchThreadsWorker(nativeDistDir) {
  const file = join(nativeDistDir, "wasi-worker.mjs");
  let code = readFileSync(file, "utf8");
  code = replaceOrThrow(
    code,
    'import { WASI } from "node:wasi";\n',
    "",
    "wasi-worker.mjs",
  );
  code = replaceOrThrow(
    code,
    'const { instantiateNapiModuleSync, MessageHandler, getDefaultContext } = require("@napi-rs/wasm-runtime");',
    'const { instantiateNapiModuleSync, MessageHandler, getDefaultContext, WASI } = require("@napi-rs/wasm-runtime");',
    "wasi-worker.mjs",
  );
  code = replaceOrThrow(
    code,
    "const wasi = new WASI({",
    "const wasi = new WASI({\n      fs,",
    "wasi-worker.mjs",
  );
  writeFileSync(file, code);
  return "wasi-worker.mjs";
}

/**
 * Rewrite the Node-target WASI loaders in `nativeDistDir` to use the portable
 * `@napi-rs/wasm-runtime` WASI so they load under Bun as well as Node. Returns
 * the list of patched file names.
 */
export function patchWasiLoadersForBun(nativeDistDir) {
  return [patchNodeLoader(nativeDistDir), patchThreadsWorker(nativeDistDir)];
}
