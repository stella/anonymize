import { COMMON_LABELS, type CommonLabel } from "./taxonomy";
import type { Prf } from "./metrics";

export type ThroughputReport = {
  readonly initSeconds: number;
  readonly totalChars: number;
  readonly coldCharsPerSec: number;
  readonly warmCharsPerSec: number;
};

export type LibraryReport =
  | {
      readonly name: string;
      readonly version: string;
      readonly status: "ok";
      readonly notes?: string | undefined;
      readonly supportedLabels: readonly CommonLabel[];
      readonly throughput: ThroughputReport;
      readonly overall: {
        readonly overlapAll: Prf;
        readonly overlapSupported: Prf;
        readonly exactAll: Prf;
      };
      readonly perLabel: Readonly<Record<CommonLabel, Prf>>;
      readonly perLanguage: Readonly<Record<string, Prf>>;
    }
  | {
      readonly name: string;
      readonly version: string;
      readonly status: "unavailable";
      readonly reason: string;
    };

export type BenchResult = {
  readonly createdAt: string;
  readonly gitSha: string;
  readonly hardware: string;
  readonly runtime: string;
  readonly corpus: {
    readonly documents: number;
    readonly entities: number;
    readonly byLanguage: Record<string, number>;
    readonly byLabel: Record<string, number>;
  };
  readonly matching: {
    readonly primary: string;
    readonly secondary: string;
  };
  readonly libraries: readonly LibraryReport[];
};

const pct = (value: number): string => (value * 100).toFixed(1);

const numberWithSep = (value: number): string =>
  Math.round(value).toLocaleString("en-US");

export const renderMarkdown = (result: BenchResult): string => {
  const lines: string[] = [];
  const ok = result.libraries.filter(
    (lib): lib is Extract<LibraryReport, { status: "ok" }> =>
      lib.status === "ok",
  );
  const languages = Object.keys(result.corpus.byLanguage).sort();

  lines.push("# stella/anonymize vs. open-source PII redaction libraries");
  lines.push("");
  lines.push(
    "Reproducible comparison of `@stll/anonymize` against other open-source PII",
    "detection/redaction libraries on recall, precision, and throughput over a",
    "public, synthetic, legal-domain corpus (en/cs/de). Generated by",
    "`bun run bench:compare` in `packages/benchmark`. See `REPRODUCING.md` for the",
    "exact toolchain, model versions, and taxonomy-mapping decisions.",
    "",
  );
  lines.push(`- Generated: ${result.createdAt}`);
  lines.push(`- Commit: ${result.gitSha}`);
  lines.push(`- Runtime: ${result.runtime}`);
  lines.push(`- Hardware: ${result.hardware}`);
  lines.push("");

  lines.push("## Corpus");
  lines.push("");
  lines.push(
    `${result.corpus.documents} documents, ${result.corpus.entities} labelled gold entities.`,
    "All text is public-safe synthetic legal prose (invented people, companies,",
    "and identifiers). Ground-truth spans are authored positionally so offsets are",
    "correct by construction; the `.snapshot.json` fixtures elsewhere in the repo",
    "are stella's own output and are deliberately NOT used as ground truth here.",
    "",
  );
  lines.push("| Language | Documents |");
  lines.push("| --- | --- |");
  for (const lang of languages) {
    lines.push(`| ${lang} | ${result.corpus.byLanguage[lang]} |`);
  }
  lines.push("");
  lines.push("| Label | Gold entities |");
  lines.push("| --- | --- |");
  for (const label of COMMON_LABELS) {
    lines.push(`| ${label} | ${result.corpus.byLabel[label] ?? 0} |`);
  }
  lines.push("");

  lines.push("## Matching");
  lines.push("");
  lines.push(`- Primary: ${result.matching.primary}`);
  lines.push(`- Secondary: ${result.matching.secondary}`);
  lines.push(
    "- One-to-one greedy assignment (highest overlap first); a predicted span",
    "  matches at most one gold span and vice versa.",
    "",
  );

  lines.push("## Overall");
  lines.push("");
  lines.push(
    "Two overall scores per library. `all-labels` counts every gold entity in the",
    "corpus. `supported-labels` counts only the categories the library can emit",
    "after taxonomy mapping, so a library is not judged on categories it never",
    "attempts (e.g. an email-only scrubber is not charged recall on dates). Both",
    "are reported; neither is cherry-picked.",
    "",
  );
  lines.push(
    "| Library | Version | Supported labels | P | R | F1 (overlap, all) | F1 (overlap, supported) | F1 (exact, all) |",
  );
  lines.push("| --- | --- | --- | --- | --- | --- | --- | --- |");
  for (const lib of ok) {
    lines.push(
      `| ${lib.name} | ${lib.version} | ${lib.supportedLabels.length}/${COMMON_LABELS.length} | ${pct(lib.overall.overlapAll.precision)} | ${pct(lib.overall.overlapAll.recall)} | ${pct(lib.overall.overlapAll.f1)} | ${pct(lib.overall.overlapSupported.f1)} | ${pct(lib.overall.exactAll.f1)} |`,
    );
  }
  lines.push("");
  lines.push("Cells are percentages. P/R/F1 use the primary overlap rule.");
  lines.push("");

  lines.push("## Per-label F1 (overlap, all-labels)");
  lines.push("");
  const header = ["Label", ...ok.map((lib) => lib.name)];
  lines.push(`| ${header.join(" | ")} |`);
  lines.push(`| ${header.map(() => "---").join(" | ")} |`);
  for (const label of COMMON_LABELS) {
    const cells = ok.map((lib) => {
      const prf = lib.perLabel[label];
      const supported = lib.supportedLabels.includes(label);
      if (!supported && prf.tp + prf.fp === 0) {
        return "n/a";
      }
      return pct(prf.f1);
    });
    lines.push(`| ${label} | ${cells.join(" | ")} |`);
  }
  lines.push("");
  lines.push(
    "`n/a` marks a label the library does not target (no recognizer maps to it).",
    "A `0.0` means the library targets the label but scored no correct match.",
    "",
  );

  lines.push("## Per-label recall (overlap, all-labels)");
  lines.push("");
  lines.push(`| ${header.join(" | ")} |`);
  lines.push(`| ${header.map(() => "---").join(" | ")} |`);
  for (const label of COMMON_LABELS) {
    const cells = ok.map((lib) => {
      const prf = lib.perLabel[label];
      const supported = lib.supportedLabels.includes(label);
      if (!supported && prf.tp === 0) {
        return "n/a";
      }
      return pct(prf.recall);
    });
    lines.push(`| ${label} | ${cells.join(" | ")} |`);
  }
  lines.push("");

  lines.push("## Per-language F1 (overlap, all-labels)");
  lines.push("");
  const langHeader = ["Library", ...languages];
  lines.push(`| ${langHeader.join(" | ")} |`);
  lines.push(`| ${langHeader.map(() => "---").join(" | ")} |`);
  for (const lib of ok) {
    const cells = languages.map((lang) =>
      pct((lib.perLanguage[lang] ?? { f1: 0 }).f1),
    );
    lines.push(`| ${lib.name} | ${cells.join(" | ")} |`);
  }
  lines.push("");
  lines.push(
    "scrubadub and redact-pii are English-only libraries; their cs/de scores are",
    "expected to be low and are reported as-is.",
    "",
  );

  lines.push("## Throughput");
  lines.push("");
  lines.push(
    "Characters per second over one full corpus pass. `cold` includes first-use",
    "lazy initialisation within the process; `warm` is a second pass at steady",
    "state. `init` is one-time setup (model load / pipeline build) measured",
    "separately. Python throughput is measured inside the Python process and",
    "excludes interpreter startup. Single run on one machine: treat as",
    "order-of-magnitude, not a precise micro-benchmark.",
    "",
  );
  lines.push("| Library | init (s) | cold (chars/s) | warm (chars/s) |");
  lines.push("| --- | --- | --- | --- |");
  for (const lib of ok) {
    lines.push(
      `| ${lib.name} | ${lib.throughput.initSeconds.toFixed(2)} | ${numberWithSep(lib.throughput.coldCharsPerSec)} | ${numberWithSep(lib.throughput.warmCharsPerSec)} |`,
    );
  }
  lines.push("");

  const unavailable = result.libraries.filter(
    (lib) => lib.status === "unavailable",
  );
  if (unavailable.length > 0) {
    lines.push("## Excluded libraries");
    lines.push("");
    for (const lib of unavailable) {
      if (lib.status === "unavailable") {
        lines.push(`- **${lib.name}**: ${lib.reason}`);
      }
    }
    lines.push("");
  }

  const notes = ok.filter((lib) => lib.notes);
  if (notes.length > 0) {
    lines.push("## Adapter notes");
    lines.push("");
    for (const lib of notes) {
      lines.push(`- **${lib.name}**: ${lib.notes}`);
    }
    lines.push("");
  }

  lines.push("## Caveats and honesty notes");
  lines.push("");
  lines.push(
    "- **Domain skew.** The corpus is legal-domain and multilingual (en/cs/de).",
    "  stella is built for exactly this domain and language mix, which favours it.",
    "  Competitors tuned for English chat/email (scrubadub, redact-pii) or general",
    "  English text (Presidio's default models) are outside their strongest",
    "  setting on cs/de. The per-language table exists so the English-only",
    "  comparison is visible on its own.",
    "- **Synthetic data.** Ground truth is authored, not sampled from real",
    "  filings, so absolute numbers may differ from production text. It is a",
    "  fairness/consistency trade: every library sees identical inputs with exact",
    "  gold spans, and no real personal data is published.",
    "- **stella configuration.** stella runs its rules pipeline with NER off (the",
    "  product default and corpus config). Enabling NER could change its numbers.",
    "- **Mapping generosity.** Taxonomy mappings are deliberately generous to",
    "  competitors (coarse buckets; Presidio's NRP/GPE folded into address). See",
    "  `src/taxonomy.ts` for every decision.",
    "- **No cherry-picking.** Every label is reported, including those where stella",
    "  underperforms. Overlap and exact modes are both shown.",
    "",
  );

  return `${lines.join("\n")}\n`;
};
