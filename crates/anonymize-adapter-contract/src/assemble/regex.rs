/// Embedded static regex table, generated from the TypeScript source of truth.
const NATIVE_REGEX_TABLE_JSON: &str = include_str!("native-regex-table.json");

/// One row of the embedded static regex table: the already-native pattern plus
/// the raw meta fields needed to reproduce `toNativeRegexMeta` (including its
/// validator gating).
#[derive(Deserialize)]
#[serde(rename_all = "camelCase")]
struct RawRegexRow {
  pattern: BindingSearchPattern,
  label: String,
  score: f64,
  #[serde(default)]
  validator_id: Option<String>,
  #[serde(default)]
  validator_input_kind: Option<String>,
  #[serde(default)]
  min_byte_length: Option<u32>,
}

/// Result of the static + signing regex assembly.
pub(super) struct RegexAssembly {
  /// `regex_patterns` prefix: static-filtered patterns then signing patterns.
  pub patterns: Vec<BindingSearchPattern>,
  /// `regex_meta`: fully reproduced (parallel to `patterns`).
  pub meta: Vec<BindingRegexMatchMeta>,
}

/// Mirrors `toNativeRegexMeta` for a static-table row, including the validator
/// gating that throws in the TypeScript source.
///
/// Static rows never carry a `sourceDetail`, so it is always absent here.
fn to_native_regex_meta(
  row: &RawRegexRow,
) -> Result<BindingRegexMatchMeta, AssembleError> {
  let mut meta = BindingRegexMatchMeta {
    label: row.label.clone(),
    score: row.score,
    ..BindingRegexMatchMeta::default()
  };
  if let Some(validator_id) = row.validator_id.as_deref() {
    // Mirrors `nativeSupportsRegexMeta`: id must be known and the input kind
    // (when present) must be one the native validator can rebuild.
    let input_supported = matches!(
      row.validator_input_kind.as_deref(),
      None | Some("digits-only" | "crypto-wallet-candidate")
    );
    if !NATIVE_REGEX_VALIDATOR_IDS.contains(&validator_id) || !input_supported {
      return Err(AssembleError::UnsupportedRegexValidator {
        validator: validator_id.to_string(),
      });
    }
    meta.requires_validation = Some(true);
    meta.validator_id = Some(validator_id.to_string());
    if let Some(kind) = row.validator_input_kind.as_ref() {
      meta.validator_input = Some(kind.clone());
    }
  }
  if let Some(min_byte_length) = row.min_byte_length {
    meta.min_byte_length = Some(min_byte_length);
  }
  Ok(meta)
}

/// `SIGNING_CLAUSE_META`: `{ label: "address", score: 0.9 }`.
fn signing_clause_meta() -> BindingRegexMatchMeta {
  BindingRegexMatchMeta {
    label: "address".to_string(),
    score: 0.9,
    ..BindingRegexMatchMeta::default()
  }
}

#[derive(Deserialize)]
struct SigningClauses {
  #[serde(default)]
  patterns: Vec<SigningClauseEntry>,
}

#[derive(Deserialize)]
#[serde(rename_all = "camelCase")]
struct SigningClauseEntry {
  #[serde(default)]
  lang: String,
  #[serde(default)]
  prefix: String,
  #[serde(default)]
  suffix: String,
  #[serde(default)]
  prepositions: Vec<String>,
  #[serde(default)]
  prefilter_phrases: Option<Vec<String>>,
}

/// Mirrors `buildSigningClausePattern`.
fn build_signing_clause_pattern(entry: &SigningClauseEntry) -> String {
  let place = if entry.prepositions.is_empty() {
    r"(\p{Lu}\p{Ll}+(?:[- ]\p{Lu}\p{Ll}+)*)".to_string()
  } else {
    let prep_alt = entry.prepositions.join("|");
    let mut place = String::from(r"(\p{Lu}\p{Ll}+(?:\s+(?:");
    place.push_str(&prep_alt);
    place.push_str(r")\s+\p{Lu}\p{Ll}+)*(?:\s+\p{Lu}\p{Ll}+)*)");
    place
  };
  let mut pattern = String::from(r"(?:^|\n|[^\S\n])");
  pattern.push_str(&entry.prefix);
  pattern.push_str(&place);
  if !entry.suffix.is_empty() {
    pattern.push_str("(?:");
    pattern.push_str(&entry.suffix);
    pattern.push(')');
  }
  pattern
}

/// Mirrors `getNativeSigningClausePatterns` + `toNativeRegexPattern`: a
/// language-scoped, lazy regex with the entry's prefilter phrases.
fn build_native_signing_patterns(
  languages: Option<&[String]>,
) -> Result<Vec<BindingSearchPattern>, AssembleError> {
  let data: SigningClauses = parse_data_file("signing-clauses.json")?;
  let mut patterns = Vec::new();
  for entry in &data.patterns {
    if !signing_clause_language_matches(&entry.lang, languages) {
      continue;
    }
    patterns.push(BindingSearchPattern {
      kind: "regex".to_string(),
      pattern: build_signing_clause_pattern(entry),
      distance: None,
      case_insensitive: None,
      whole_words: None,
      lazy: Some(true),
      // `entry.prefilterPhrases ?? []` is always assigned, so the field is
      // present (possibly empty) rather than absent.
      prefilter_any: Some(entry.prefilter_phrases.clone().unwrap_or_default()),
      prefilter_case_insensitive: Some(false),
      prefilter_regex: None,
      prefilter_window_bytes: Some(CONTEXT_REGEX_PREFILTER_WINDOW_BYTES),
      prepared_artifact_policy: Some(BindingPreparedArtifactPolicy::Omit),
    });
  }
  Ok(patterns)
}

/// # Errors
///
/// Returns [`AssembleError`] when the embedded regex table or
/// `signing-clauses.json` fails to parse, or when a static regex references a
/// validator the native config cannot support.
pub(super) fn build_regex(
  ctx: &AssembleContext<'_>,
) -> Result<RegexAssembly, AssembleError> {
  let mut patterns = Vec::new();
  let mut meta = Vec::new();

  if ctx.enable_regex() {
    let table: Vec<RawRegexRow> = serde_json::from_str(NATIVE_REGEX_TABLE_JSON)
      .map_err(|error| AssembleError::DataParse {
        name: "native-regex-table.json".to_string(),
        message: error.to_string(),
      })?;
    for row in &table {
      if !ctx.label_allowed(&row.label) {
        continue;
      }
      patterns.push(row.pattern.clone());
      meta.push(to_native_regex_meta(row)?);
    }

    // Native signing-clause patterns: `enableRegex && labelIsAllowed("address")`,
    // scoped to the content languages.
    if ctx.label_allowed("address") {
      let signing =
        build_native_signing_patterns(ctx.content_languages.as_deref())?;
      for pattern in signing {
        patterns.push(pattern);
        meta.push(signing_clause_meta());
      }
    }
  }

  Ok(RegexAssembly { patterns, meta })
}
