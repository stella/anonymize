//! `regex_meta` (full) and `regex_patterns` (partial prefix): ports the static
//! regex assembly from `detectors/regex.ts` and `build-unified-search.ts`.
//!
//! # What is assembled
//!
//! `buildNativeStaticConfig` builds `regex_patterns` from `allRegex` (static
//! defs + dynamically generated currency/date/signing patterns) minus the
//! `omitRegexRanges` (currency, date, and the `getSigningClausePatterns`
//! output), then appends the `getNativeSigningClausePatterns` output, then the
//! legal-form patterns (permanently empty after the v2 rewrite) and the trigger
//! patterns. The parallel `regex_meta` covers the surviving static defs plus one
//! `SIGNING_CLAUSE_META` per native signing pattern (legal-form and trigger
//! patterns get NO meta).
//!
//! So the C1 boundary is:
//! - `regex_meta` = static-defs-filtered-by-label ++ signing (FULL)
//! - `regex_patterns` = static-defs-filtered-by-label ++ signing ++
//!   \[legal-form (empty) ++ trigger (slice C2)\] (PARTIAL)
//!
//! `regex_meta` is fully reproduced here. `regex_patterns` reproduces the prefix
//! (everything up to the trigger tail); the trigger patterns come from
//! `trigger_data`, a later slice, so `regex_patterns` stays in `FIELDS_PENDING`
//! and the parity harness compares only the prefix (see the `assemble_parity`
//! test). The prefix length equals `regex_meta.len()`.
//!
//! # Static regex table
//!
//! The static regex source strings are derived at TypeScript module load from
//! the `@stll/stdnum` validators (`toRegex(validator).source`), so they cannot
//! be hand-transcribed. They are captured verbatim into the embedded
//! `native-regex-table.json` artifact (regenerated by
//! `packages/anonymize/scripts/capture-assemble-fixtures.mjs`). The label
//! filtering, validator gating, and signing-clause pattern generation below are
//! ported logic; the fixtures cross-check the embedded table byte-for-byte.

